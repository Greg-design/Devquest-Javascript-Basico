// SOMA

function somar(numero1, numero2){
    let soma =  numero1 + numero2
    return "A soma dos dois números é: " + soma
}

// DIVISÃO

function divisao(numero1, numero2){
    let dividindo =  numero1 / numero2
    return "A divisão dos dois números é: " + dividindo
}

// MULTIPLICAÇÃO

function multiplicacao(numero1, numero2){
    let multiplicando = numero1 * numero2
    return "A multiplicação dos dois números é: " + multiplicando
}

//SUBTRAÇÃO

function subtracao(numero1, numero2){
    let subtraindo = numero1 - numero2
    return "A subtração dos dois números é: " + subtraindo
}

//---------------------------------------------------------------

let resultadoDaSoma = somar(7, 7)
console.log(resultadoDaSoma)

let resultadoDaDivisao = divisao(10, 2)
console.log(resultadoDaDivisao)

let resultadoDaMultiplicacao = multiplicacao(4, 8)
console.log(resultadoDaMultiplicacao)

let resultadoDaSubtracao = subtracao(12, 5)
console.log(resultadoDaSubtracao)



